#include "Ice.hpp"

/* -------------------------------------------------------------------------- */
/* CONSTRUCTORS                                                               */
/* -------------------------------------------------------------------------- */

Ice::Ice(void)
{
	_type = "ice";
}

Ice::Ice(Ice const & src)
{
	*this = src;
}

/* -------------------------------------------------------------------------- */
/* DESTRUCTOR                                                                 */
/* -------------------------------------------------------------------------- */

Ice::~Ice(void)
{
}

/* -------------------------------------------------------------------------- */
/* MEMBER FUNCTIONS                                                           */
/* -------------------------------------------------------------------------- */

AMateria	*Ice::clone() const
{
	AMateria *	ice = new Ice;
	return (ice);
}

void	Ice::use(ICharacter & target)
{
	std::cout << "* shoots an ice bolt at " << target.getName() << " *" << std::endl;
}

/* -------------------------------------------------------------------------- */
/* OPERATORS                                                                  */
/* -------------------------------------------------------------------------- */

Ice	&Ice::operator=(Ice const & rhs)
{
	if (this != &rhs)
		_type = rhs.getType();
	return (*this);
}