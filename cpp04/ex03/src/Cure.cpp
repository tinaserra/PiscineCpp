#include "Cure.hpp"

/* -------------------------------------------------------------------------- */
/* CONSTRUCTORS                                                               */
/* -------------------------------------------------------------------------- */

Cure::Cure(void)
{
	_type = "cure";
}

Cure::Cure(Cure const & src)
{
	*this = src;
}

/* -------------------------------------------------------------------------- */
/* DESTRUCTOR                                                                 */
/* -------------------------------------------------------------------------- */

Cure::~Cure(void)
{
}

/* -------------------------------------------------------------------------- */
/* MEMBER FUNCTIONS                                                           */
/* -------------------------------------------------------------------------- */

AMateria	*Cure::clone() const
{
	AMateria *	cure = new Cure;
	return (cure);
}

void	Cure::use(ICharacter & target)
{
	std::cout << "* heals " << target.getName() << "'s wounds *" << std::endl;
}

/* -------------------------------------------------------------------------- */
/* OPERATORS                                                                  */
/* -------------------------------------------------------------------------- */

Cure	&Cure::operator=(Cure const & rhs)
{
	if (this != &rhs)
		_type = rhs.getType();
	return (*this);
}